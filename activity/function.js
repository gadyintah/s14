	function ibalikAngDagdagan(unangNumero, ikalawangNumero){
		return unangNumero + ikalawangNumero;
	}

	let angDagdagan = ibalikAngDagdagan(1, 2);
	console.log("Ang Dagdagan ay " + angDagdagan);

	function ibalikAngBawasan(unangNumero, ikalawangNumero){
		return unangNumero - ikalawangNumero;
	}

	let angBawasan = ibalikAngBawasan(1, 2);
	console.log("Ang Bawasan ay " + angBawasan);


	function ibalikAngParamihan(unangNumero, ikalawangNumero){
		return unangNumero * ikalawangNumero;
	}

	let angParamihan = ibalikAngParamihan(1, 2);
	console.log("Ang Paramihan ay " + angParamihan);

